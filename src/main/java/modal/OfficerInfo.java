package modal;

public class OfficerInfo implements java.io.Serializable {

	private String officerUsername;
	private String officerName;
	private String officerRole;
	private String officerContact;
	private String officerEmail;
	private String officerPassword;
	private String officerCompany;
	
	public String getOfficerCompany() {
		return officerCompany;
	}
	public void setOfficerCompany(String officerCompany) {
		this.officerCompany = officerCompany;
	}
	public String getOfficerUsername() {
		return officerUsername;
	}
	public void setOfficerUsername(String officerUsername) {
		this.officerUsername = officerUsername;
	}
	public String getOfficerName() {
		return officerName;
	}
	public void setOfficerName(String officerName) {
		this.officerName = officerName;
	}
	public String getOfficerRole() {
		return officerRole;
	}
	public void setOfficerRole(String officerRole) {
		this.officerRole = officerRole;
	}
	public String getOfficerContact() {
		return officerContact;
	}
	public void setOfficerContact(String officerContact) {
		this.officerContact = officerContact;
	}
	public String getOfficerEmail() {
		return officerEmail;
	}
	public void setOfficerEmail(String officerEmail) {
		this.officerEmail = officerEmail;
	}
	public String getOfficerPassword() {
		return officerPassword;
	}
	public void setOfficerPassword(String officerPassword) {
		this.officerPassword = officerPassword;
	}
	

}
